// REPL = Read Eval Print Loop 
// REPL is a runtime environment with give features like read eval print and loop.

    // Read: Accept input from the user (e.g., code or commands).
    // Eval: Evaluate or execute the input code.
    // Print: Display the output or result of the executed code.
    // Loop: Wait for and repeat the process for the next input.

// Features of REPL:
    // Immediate Feedback: Execute code interactively and get results instantly.
    // Testing Environment: Ideal for testing small snippets of code.
    // Error Handling: Displays errors immediately for debugging.
    // Last Result Reference: Use _ to access the last evaluated result.
    // Access to Node.js Modules: Import and use modules directly in the REPL.
    // Multiline Input: Enter multiline expressions or functions using parentheses.

